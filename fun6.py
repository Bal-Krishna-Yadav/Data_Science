# ➕ Addition with 4 positional arguments
def add_num(a, b, c, d):
    sum = a + b + c + d
    print("Sum of four numbers:", sum)
add_num(10, 20, 30, 40)

# ➖ Subtraction with 2 positional arguments
def sub_num(x, y):
    sub = x - y
    print("Sub of two numbers:", sub)
sub_num(50, 20)

# ✖️ Multiplication with 2 positional arguments
def mul_num(e, f):
    mul = e * f
    print("Mul of two numbers:", mul)
mul_num(5, 5)

# ➗ Division with 2 positional arguments
def div_num(g, h):
    div = g / h
    print("Div of two numbers:", div)
div_num(20, 10)